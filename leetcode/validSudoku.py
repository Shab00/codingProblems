def isValid(board: list[list[str]]) -> bool:

    for rows in board:
        row = set()
        r = []
        for i in rows:
            if i != '.':
                row.add(i)
                r.append(i)
        if len(r) != len(row):
            return False

    for rows in range(len(board[0])):
        cols = set()
        c = []
        for col in range(len(board)):
            if board[col][rows] != '.':
                cols.add(board[col][rows])
                c.append(board[col][rows])
        if len(c) != len(cols):
            return False

    return True

